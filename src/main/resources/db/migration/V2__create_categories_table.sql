CREATE TABLE categories (
                            id          BIGINT       NOT NULL AUTO_INCREMENT,
                            name        VARCHAR(100) NOT NULL,
                            description VARCHAR(255),
                            parent_id   BIGINT,
                            PRIMARY KEY (id),
                            UNIQUE KEY uk_categories_name (name),
                            FOREIGN KEY (parent_id) REFERENCES categories (id)
);

